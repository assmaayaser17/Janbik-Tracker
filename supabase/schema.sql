create extension if not exists pgcrypto;

create table if not exists public.pilot_rows (
  id uuid primary key default gen_random_uuid(),
  name text not null default '',
  district text default '',
  phone text default '',
  rating numeric default 0,
  reviews integer default 0,
  status text default 'لم يتم التواصل',
  owner text default '—',
  last text default '',
  desire text default '',
  notes text default '',
  position double precision default 0,
  updated_at timestamptz default now()
);

alter table public.pilot_rows enable row level security;

-- anyone (including people who just have the link, not logged in) can VIEW the board
drop policy if exists "public read" on public.pilot_rows;
create policy "public read" on public.pilot_rows
  for select using (true);

-- only signed-in team members can add / edit / delete rows
drop policy if exists "team insert" on public.pilot_rows;
create policy "team insert" on public.pilot_rows
  for insert to authenticated with check (true);

drop policy if exists "team update" on public.pilot_rows;
create policy "team update" on public.pilot_rows
  for update to authenticated using (true) with check (true);

drop policy if exists "team delete" on public.pilot_rows;
create policy "team delete" on public.pilot_rows
  for delete to authenticated using (true);

-- keep updated_at fresh automatically
create or replace function public.set_updated_at()
returns trigger language plpgsql as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

drop trigger if exists trg_pilot_rows_updated on public.pilot_rows;
create trigger trg_pilot_rows_updated
before update on public.pilot_rows
for each row execute function public.set_updated_at();

-- turn on realtime so every open tab updates instantly when someone edits
alter publication supabase_realtime add table public.pilot_rows;

